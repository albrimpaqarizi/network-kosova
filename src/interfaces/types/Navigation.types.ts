import { BottomTabScreenProps } from "@react-navigation/bottom-tabs";
import {
  CompositeScreenProps,
  NavigatorScreenParams,
} from "@react-navigation/native";
import { NativeStackScreenProps } from "@react-navigation/native-stack";

declare global {
  namespace ReactNavigation {
    interface RootParamList extends RootStackParamList {}
  }
}

export type RootStackParamList = {
  Root: NavigatorScreenParams<RootTabParamList> | undefined;
  login: NavigatorScreenParams<Pick<NonAuthTabParamList, "login">>;
  register: NavigatorScreenParams<Pick<NonAuthTabParamList, "register">>;
  Modal: undefined;
  NotFound: undefined;
};

export type RootStackScreenProps<Screen extends keyof RootStackParamList> =
  NativeStackScreenProps<RootStackParamList, Screen>;

export type RootTabParamList = {
  TabOne: undefined;
  TabTwo: undefined;
};
export type NonAuthTabParamList = {
  login: undefined;
  register: undefined;
};

export type RootTabScreenProps<Screen extends keyof RootTabParamList> =
  CompositeScreenProps<
    BottomTabScreenProps<RootTabParamList, Screen>,
    NativeStackScreenProps<RootStackParamList>
  >;
export type NonAuthTabScreenProps<Screen extends keyof NonAuthTabParamList> =
  CompositeScreenProps<
    BottomTabScreenProps<NonAuthTabParamList, Screen>,
    NativeStackScreenProps<NonAuthTabParamList>
  >;
